    private void jButtonloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonloginActionPerformed

                String input = jTextField1.getText();
        try {
            if (input.matches(".*[a-zA-Z].*")) {
              JOptionPane.showMessageDialog(null, "Name accepted");
        } else {
             JOptionPane.showMessageDialog(null, "Name unaccepted");
             jTextField1.setText("");
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(null, "An error occurred: " + ex.getMessage());
        }
        
        try {
            int pax = Integer.parseInt(jTextField2.getText());
            if (pax <= 0) {
                JOptionPane.showMessageDialog(null, "Please enter real pax number");
                jTextField2.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Pax accepted: " + pax);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter numbers only");
            jTextField2.setText("");
        }
        
        try {
    if (jTextField1.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Please fill out the username.");
    } else if (jTextField2.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Please fill out number of pax.");
    } else {
        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(null, "Choose dine in or take away");
        } else {
            order click = new order();
            click.setVisible(true);
            setVisible(false);
        }
    }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Unexpected Error occurred: " + ex.getMessage());
        }
    
    }//GEN-LAST:event_jButtonloginActionPerformed

